            <p class="txt">土台(コア)とは、歯の強度をあげるために設置するものです。神経を取り除いた歯に用います。</p>
            <p class="txt">神経を抜いた歯は、(その処置によって)歯の根の中が空洞になっており、強度が低下しています。そこで土台を設置し、補強するわけです。</p>
            <p class="txt">歯に神経がある場合には土台は必要ありません。</p>
