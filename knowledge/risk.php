        </div>
        <div class="wrapper">
            <h3 class="h3a">歯根吸収ってなに？</h3>
            <p class="txta">歯根吸収とは、歯の根っこが短くなる現象です。歯根吸収は奥歯よりも前歯に起こりやすく、矯正治療をおこなうと少なからず発生します。重度の歯根吸収はグラつきや脱落の原因になってしまいます。</p>

            <h3 class="h3a">歯根吸収の原因</h3>
            <div class="gap30">
                <p class="txta">
                    <em>原因は過剰な矯正力です</em>。詳しく説明します。<br>
                    歯に矯正力が加わると、歯根膜（歯と歯槽骨の間のクッション）の片側が圧縮され（圧縮側）、反対側が引き伸ばされます（牽引側）。<br>
                    圧縮側では、歯槽骨に「破骨細胞」と呼ばれる細胞が集まり歯槽骨を吸収します。（このプロセスによって歯は新しい位置へ移動していきます。）<br>
                    しかし<em>圧縮が過剰であったり長期間続いたりすると、歯根膜の細胞組織が酸素不足・栄養不足に陥って壊死します</em>。すると歯根膜はクッション機能を失い、歯根と歯槽骨が異常な圧力を受けることになります。<em>これによって破骨細胞は異常に活性化し、歯槽骨のみならず歯根表面にも作用</em>しはじめます。結果、歯根吸収が起こるのです。
                </p>
                <p class="txta">
                    とはいっても「過剰な矯正力」がどの程度なのかを数値化するのは難しく「これ以上負荷をかけなければ起こらない」という安全数値のようなものがあるわけではありません。それゆえ歯根吸収を100％防ぐことは困難で、少なからず起こってしまいます。<br>
                    多少の歯根吸収であれば問題ないことがほとんどです。大事なのは、兆候を発見したときの対処です。
                </p>
