            <h2 class="ttl">マウスピースで<br>歯が動く仕組み</h2>
        </div>
        <div class="wrapper">
            <h3 class="h3a">負荷をかけて歯を動かす</h3>
            <p class="txta">矯正は、<a href="/knowledge/principle#sec1">歯に「負荷＝矯正力」をかけて歯槽骨を代謝させる</a>ことによって実現します。この原則はマウスピース矯正も同じです。<br>そしてマウスピース矯正では、この「矯正力」をマウスピースによって生み出します。</p>

            <h3 class="h3a">マウスピースの「ズレ」で<br>矯正力をかける</h3>
            <div class="gap30">
                <p class="txta">マウスピース矯正における矯正力の源泉は「マウスピースのズレ」です。詳しく説明しましょう。下の図を見てください。</p>
                <picture class="picc">
                    <source type="image/webp" srcset="/assets/img/orthodontics/about_mouthpiece/sec2_1.png.webp">
                    <img src="/assets/img/orthodontics/about_mouthpiece/sec2_1.png" alt="" width="350" height="192">
                </picture>
                <p class="txta">①が動かしたい歯だとします（移動歯）。<br>マウスピースを作る際、マウスピースの①の部分だけをやや右にずらして作っておきます。<br>このマウスピースを装着すると、①の歯には右方向への矯正力が働き、それ以外の歯（固定源）には左向きの力が働きます。結果、歯1本に対してより多くの力がかかる①だけが右に動き、その他の歯の位置はキープされます。</p>
                <p class="txta">このように、大部分の歯を固定源にして移動歯に矯正力をかけることで、歯を移動していきます。</p>
            </div>

            <h3 class="h3a">2週間ごとに<br>マウスピースを着け替える</h3>
            <div class="gap30">
                <p class="txta">マウスピースで歯に矯正力をかけ続けると、歯は2週間で最大0.25mmほど動きます。すると、歯の移動によって歯並びとマウスピースが同じ形になり、今のマウスピースでは矯正力をかけられなくなります。そこでさらにズレを大きくした新しいマウスピースに交換し、さらに歯を動かしていきます。<br>このように2週間に一度のペースでマウスピースを交換していき、目的の場所に歯が動くまで矯正力をかけ続けます。</p>
                <picture class="picc">
                    <img src="/assets/img/orthodontics/about_mouthpiece/sec2_2.png" alt="" width="350" height="71">
                </picture>
            </div>

            <h3 class="h3a">移動歯と固定源を入れ替える</h3>
            <p class="txta">実際には、このマウスピース交換の過程で「移動歯」と「固定源」を入替えていきます。<br>①まずは奥歯を固定源にして前歯を動かし…<br>②次に前歯部を固定元にして奥歯を動かす…<br>という感じです。<br>このように順番に歯を動かして歯並びを改善していきます。</p>
